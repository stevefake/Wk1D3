puts "Please enter the dealer's card: "
user_input = gets.chomp

player_card_1 = player_hand[0]
player_card_2 = player_hand[1]
player_hand_sum = player_card_1.to_i + player_card_2.to_i
dealer_hand = user_input

print "Your optimal move is to "

if player_hand.uniq.count == 1  # Checks if array index [0, 1] are equal
  if player_hand.include?("A")
    if dealer_hand == "A"
      puts pair_solutions["A,A"][dealer_hand]
    else
      puts pair_solutions["A,A"][dealer_hand.to_i]
    end
  else
    puts pair_solutions[player_hand_sum][dealer_hand.to_i]
  end
elsif player_hand.include?("A")
  player_hand.map! { |e| e == "A" ? 11 : e }.flatten! # Replaces "A" with 11
  player_card_1_soft = player_hand[0]
  player_card_2_soft = player_hand[1]
