    puts soft_solutions[player_soft_hand_sum][dealer_hand.to_i]
  end
else
  if dealer_hand == "A"
    puts hard_solutions[player_hand_sum.to_i][dealer_hand]
  else
    puts hard_solutions[player_hand_sum.to_i][dealer_hand.to_i]
  end
end
